package fundamentos.operadores;

public class DesafioLogico 
{
	public static void main(String[] args) 
	{
		//Trabalho na terca 
		//Trabalho na quinta 
		
		boolean trabalho1 = false;
		boolean trabalho2 = false;
		
		System.out.println(trabalho1 && trabalho2);
		System.out.println("Comprou tv de 50 polegadas e tomou sorvete");
		System.out.println(trabalho1 ^ trabalho2);
		System.out.println("Comprou TV de 32 poelgadas e tomou sorvete");
		System.out.println(!(trabalho1 || trabalho2));
		System.out.println("Ficou em casa porque não fez nenhum serviço e teve uma vida mais saudável.");
	}
}
